package ext2;

public class Calculadora {

    static double calcularSuma (double x, double y) {
        return x + y;
    }

    static double calcularSuma (double x, double y, double z) {
        return x + y + z;
    }

    static int calcularSuma (int x, int y){
        return x + y;
    }

    static int calcularSuma (int x, int y, int z){
        return x + y + z;
    }
}
